"""
A set of helper functions
"""


def helper_func_a(num1, num2):
    """
    helper function a

    :param num1: first num to add
    :param num2: second num to add

    :return: int, total of the input
    """
    return num1 + num2


def helper_func_b(message=None):
    """
    Print the provided message maybe

    :param message: The message to be printed, Default is None
    :type message: str or None
    """
    print(message)
